/// A class that defines standard padding values used throughout the app.
abstract class AppPaddings {
  /// The left padding for backdrops.
  static const double backdropLeft = 80.0;

  /// The padding for images.
  static const double image = 20.0;

  /// The short padding for texts.
  static const double textsShort = 10.0;

  /// The large padding for texts, which is twice the short padding.
  static const double textsLarge = textsShort * 2;
}
