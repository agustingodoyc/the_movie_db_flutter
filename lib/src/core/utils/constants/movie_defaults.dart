/// A class that defines default values for movie properties.
class MovieDefaults {
  // Default values for boolean properties.
  static const bool adult = false;
  static const bool video = false;

  // Default values for integer properties.
  static const int id = 0;
  static const int voteCount = 0;

  // Default values for numeric properties.
  static const num voteAverage = 0.0;
  static const double popularity = 0.0;

  // Default values for string properties.
  static const String originalLanguage = 'Original Language';
  static const String originalTitle = 'Original Title';
  static const String overview = 'Overview';
  static const String releaseDate = 'Release Date';
  static const String title = 'Title';
}
